/**
 * Build complete system prompt for a given language
 * @param {string} lang - "en", "es", or "both"
 * @returns {Array} Messages array with cached and dynamic blocks
 */
export function buildSystemPrompt(lang = "en") {
  return [
    {
      role: "system",
      content: [
        {
          // This block is cached — same across all requests
          type: "text",
          text: SYSTEM_IDENTITY,
          cache_control: { type: "ephemeral" }
        },
        {
          // This block changes per user language preference
          type: "text",
          text: LANGUAGE_INSTRUCTIONS[lang] || LANGUAGE_INSTRUCTIONS.en
        }
      ]
    }
  ];
}

/**
 * Get disclaimer in appropriate language
 * @param {string} lang - "en", "es", or "both"
 * @returns {string} Disclaimer text
 */
export function getDisclaimer(lang = "en") {
  if (lang === "both") {
    return `${DISCLAIMERS.en}\n\n${DISCLAIMERS.es}`;
  }
  return DISCLAIMERS[lang] || DISCLAIMERS.en;
}
